package MyCodes;

public class RightShiftOperator {
    public static void main(String[] args) {
        System.out.println(10 >> 2); //10/2^2=40//
        System.out.println(10 >> 3); //10/2^2=40//
        System.out.println(10 >> 4); //10/2^2=40//
        System.out.println(40 >> 5); //40/2^2=40//
    }
}
