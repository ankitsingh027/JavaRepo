package MyCodes;

public class LeftShiftOperator {
    public static void main(String[] args) {
        System.out.println(10<<2); //10*2^2=40//
        System.out.println(10<<3); //10*2^2=40//
        System.out.println(10<<4); //10*2^2=40//
        System.out.println(10<<5); //10*2^2=40//
    }
}
